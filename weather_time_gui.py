LAND_GRID = [[ch == "#" for ch in row] for row in _MASTER_MAP_ROWS]

def _sample_land(row, col, out_rows, out_cols):
    """Toma la celda tierra/mar mas cercana de la grilla maestra, para una
    grilla de salida de out_rows x out_cols (la resolucion real de pantalla)."""
    mr = min(MASTER_ROWS - 1, int(row / out_rows * MASTER_ROWS))
    mc = min(MASTER_COLS - 1, int(col / out_cols * MASTER_COLS))
    return LAND_GRID[mr][mc]

def draw_world_map(canvas):
    canvas.delete("all")
    w = canvas.winfo_width()
    h = canvas.winfo_height()
    if w < 10 or h < 10:  # todavía no se renderizó (ej: al arrancar)
        w, h = 720, 320
    w -= 8
    h -= 8

    # Franja reservada arriba para los números de huso horario (UTC)
    header_h = 24

    # El mapa debe mantener proporción real 2:1 (ancho:alto). Calculo el bloque
    # más grande que respeta esa proporción sin salirse del espacio disponible,
    # sea cual sea la forma del panel (ancho y bajo, o angosto y alto).
    available_h = max(10, h - header_h)
    block_w = max(20, min(w, 2 * available_h))
    block_h = block_w / 2

    # Resolución de la grilla mostrada en pantalla (siempre 2:1, se toma de
    # la grilla maestra por muestreo del vecino más cercano).
    DISPLAY_COLS = 140
    DISPLAY_ROWS = 70
    cell = block_w / DISPLAY_COLS  # celda cuadrada: ancho == alto

    x0 = 4 + (w - block_w) / 2
    y0 = 4 + header_h + (available_h - block_h) / 2

    # Encabezado con los offsets UTC (-12 ... +11), repartidos a lo largo del bloque
    for i in range(24):
        offset = i - 12
        label = f"{offset:+d}" if offset != 0 else "0"
        x = x0 + (i + 0.5) * (block_w / 24)
        canvas.create_text(x, y0 - header_h / 2, text=label, font=("Arial", 10, "bold"), fill="#3a5a6c")

    # Tierra: un cuadrado por celda, un solo color
    for row in range(DISPLAY_ROWS):
        for col in range(DISPLAY_COLS):
            if _sample_land(row, col, DISPLAY_ROWS, DISPLAY_COLS):
                rx0 = x0 + col * cell
                ry0 = y0 + row * cell
                canvas.create_rectangle(
                    rx0, ry0, rx0 + cell, ry0 + cell,
                    fill="#4a7a96", outline="#eaf2f8"
                )

    # Guardo las métricas para poder ubicar los marcadores alineados con la grilla
    canvas.map_metrics = (x0, y0, cell, DISPLAY_COLS, DISPLAY_ROWS)


def place_marker(canvas, lat, lon, color, label):
    metrics = getattr(canvas, "map_metrics", None)
    if metrics is None:
        return
    x0, y0, cell, cols, rows = metrics
    col = min(cols - 1, max(0, int((lon + 180) / 360 * cols)))
    row = min(rows - 1, max(0, int((90 - lat) / 180 * rows)))
    x = x0 + col * cell + cell / 2
    y = y0 + row * cell + cell / 2

    r = max(3, cell * 0.8)
    canvas.create_oval(x - r, y - r, x + r, y + r, fill=color, outline="black")
    canvas.create_text(x, y - r - 6, text=label, font=("Arial", 8, "bold"), fill=color)

