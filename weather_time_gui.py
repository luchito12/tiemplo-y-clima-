def get_location():
    """Geolocalización por IP. Gratis, sin API key."""
    response = requests.get("http://ip-api.com/json/", timeout=8)
    data = response.json()
    if data.get("status") != "success":
        raise Exception("No se pudo determinar la ubicación por IP.")
    return {
        "city": data.get("city", "Desconocido"),
        "lat": data.get("lat"),
        "lon": data.get("lon"),
        "timezone": data.get("timezone"),
    }

def get_weather(lat, lon):
    """Clima actual vía Open-Meteo. Gratis, sin API key."""
    url = (
        "https://api.open-meteo.com/v1/forecast"
        f"?latitude={lat}&longitude={lon}"
        "&current=temperature_2m,relative_humidity_2m"
    )
    response = requests.get(url, timeout=8)
    data = response.json()
    current = data.get("current", {})
    if "temperature_2m" not in current:
        raise Exception("Respuesta inesperada de Open-Meteo: " + str(data))
    return current["temperature_2m"], current["relative_humidity_2m"]

def get_local_time(timezone_str):
    tz = ZoneInfo(timezone_str)
    return datetime.now(tz).strftime("%Y-%m-%d %H:%M:%S")

def get_timezone_label(timezone_str):
    """Devuelve algo como 'America/Argentina/Buenos_Aires (UTC-03:00)',
    calculando el offset actual (considera horario de verano si corresponde)."""
    tz = ZoneInfo(timezone_str)
    offset = datetime.now(tz).utcoffset()
    total_minutes = int(offset.total_seconds() // 60)
    sign = "+" if total_minutes >= 0 else "-"
    total_minutes = abs(total_minutes)
    hh, mm = divmod(total_minutes, 60)
    return f"{timezone_str} (UTC{sign}{hh:02d}:{mm:02d})"

