my_location = None  # se completa la primera vez que se consigue la ubicación

def refresh_data():
    """Trae ubicación + clima + hora de TU ubicación y actualiza la interfaz."""
    global my_location
    try:
        status_label.config(text="Actualizando...")
        root.update_idletasks()

        location = get_location()
        my_location = location
        temperature, humidity = get_weather(location["lat"], location["lon"])
        local_time = get_local_time(location["timezone"])

        weather_label.config(
            text=(
                f"Ubicación: {location['city']}\n"
                f"Temperatura: {temperature}°C\n"
                f"Humedad: {humidity}%\n"
                f"Hora local: {local_time}\n"
                f"Zona horaria: {get_timezone_label(location['timezone'])}"
            )
        )
        status_label.config(text="")
        redraw_map()
    except requests.exceptions.SSLError as e:
        weather_label.config(text="Error de certificado SSL.\nVer error.log junto al .exe.")
        status_label.config(text="")
        log_error("refresh_data - SSL", e)
    except requests.exceptions.RequestException as e:
        weather_label.config(text="Error de conexión a internet.\nVer error.log junto al .exe.")
        status_label.config(text="")
        log_error("refresh_data - conexión", e)
    except Exception as e:
        weather_label.config(text=f"Error al obtener datos: {e}")
        status_label.config(text="")
        log_error("refresh_data", e)

def schedule_auto_update():
    global _after_job_id
    if _after_job_id is not None:
        root.after_cancel(_after_job_id)
    config = load_config()
    interval_ms = max(1, config.get("interval_minutes", DEFAULT_INTERVAL_MIN)) * 60 * 1000
    _after_job_id = root.after(interval_ms, _auto_update_tick)

def _auto_update_tick():
    refresh_data()
    schedule_auto_update()

def manual_refresh():
    refresh_data()

def save_interval():
    try:
        minutes = int(interval_entry.get())
        if minutes <= 0:
            raise ValueError
    except ValueError:
        messagebox.showwarning("Intervalo inválido", "Ingresá un número entero mayor a 0.")
        return
    save_config(minutes)
    schedule_auto_update()
    messagebox.showinfo("Guardado", f"Intervalo de actualización: cada {minutes} min.")

