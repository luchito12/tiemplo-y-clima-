def app_dir():
    """Carpeta donde vive el .exe (o el .py si se corre como script). Ahí se
    guardan config.json y el log de errores."""
    if getattr(sys, "frozen", False):
        return os.path.dirname(sys.executable)
    return os.path.abspath(".")


def log_error(context, exc):
    """Guarda el error completo (con traceback) en error.log, al lado del
    .exe, porque al correr con --windowed no hay consola donde verlo."""
    try:
        log_path = os.path.join(app_dir(), "error.log")
        with open(log_path, "a", encoding="utf-8") as f:
            f.write(f"\n--- {datetime.now().isoformat()} | {context} ---\n")
            f.write(traceback.format_exc())
    except Exception:
        pass

