# Redibuja el mapa al cambiar el tamaño de la ventana, con un pequeño retraso
# (debounce) para no recalcular en cada pixel mientras se arrastra el borde.
_resize_after_id = None

def _on_canvas_resize(event=None):
    global _resize_after_id
    if _resize_after_id is not None:
        root.after_cancel(_resize_after_id)
    _resize_after_id = root.after(120, redraw_map)

map_canvas.bind("<Configure>", _on_canvas_resize)

if __name__ == "__main__":
    draw_world_map(map_canvas)
    refresh_data()
    schedule_auto_update()
    root.mainloop()